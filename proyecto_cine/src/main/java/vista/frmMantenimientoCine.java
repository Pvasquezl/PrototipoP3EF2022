    public void llenadoDeCombos() {
        daoCine cineDAO = new daoCine();
        List<clsCines> cines = cineDAO.select();
        cbox_aplicacion.addItem("Seleccione una opci√≥n");
        for (int i = 0; i < cines.size(); i++) {
            cbox_aplicacion.addItem(String.valueOf(cines.get(i).getiId_cine()));
        }
    }

    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Cine");
        modelo.addColumn("Nombre");
        modelo.addColumn("Direccion");

        daoCine cineDAO = new daoCine();
        List<clsCines> cines = cineDAO.select();
        tablaVendedores.setModel(modelo);
        String[] dato = new String[3];
        for (int i = 0; i < cines.size(); i++) {
            dato[0] = Integer.toString(cines.get(i).getiId_cine());
            dato[1] = cines.get(i).getsNombre();
            dato[2] = cines.get(i).getsDireccion();
            
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

    public void buscarClientes() {
        clsCines cineAConsultar = new clsCines();
        daoCine cineDAO = new daoCine();
        cineAConsultar.setiId_cine(Integer.parseInt(txtbuscado.getText()));
        cineAConsultar = cineDAO.query(cineAConsultar);
        txtNombre.setText(cineAConsultar.getsNombre());
        txtdireccion.setText(cineAConsultar.getsDireccion());
    }

